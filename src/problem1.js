function 곱하기(수_배열) {
  return 수_배열.reduce((합, 수) => 합 * 수, 1);
}

function 함수_호출_결과_생성하기(배열, 함수_배열) {
  return 함수_배열.reduce((함수_결과_배열, 함수) => {
    함수_결과_배열.push(함수(배열));

    return 함수_결과_배열;
  }, []);
}

function 연속하는_수의_배열이면(수_배열) {
  const [시작_수] = 수_배열;

  for (let 인덱스 = 1; 인덱스 < 수_배열.length; 인덱스 += 1) {
    if (수_배열[인덱스] !== 시작_수 + 인덱스) return false;
  }

  return true;
}

function 배열이_범위에_속한다면(시작, 끝) {
  const 범위에_속한다면 = _.범위_함수_생성하기(시작, 끝);

  return (배열) => 배열.every(범위에_속한다면);
}

function 예외라면(수_배열, { 시작_페이지, 끝_페이지 }) {
  const 규칙_함수_배열 = [
    연속하는_수의_배열이면,
    배열이_범위에_속한다면(시작_페이지, 끝_페이지),
  ];
  const 규칙_결과_배열 = 함수_호출_결과_생성하기(수_배열, 규칙_함수_배열);

  return 규칙_결과_배열.some((규칙_결과) => !규칙_결과);
}

function 덧셈_곱셈_구하기(수_배열) {
  return 함수_호출_결과_생성하기(수_배열, [_.더하기, 곱하기]);
}

function 연산_결과_구하기(수_배열) {
  return 수_배열
    .map(_.수_분해하기)
    .map(덧셈_곱셈_구하기)
    .flatMap((덧셈_곱셈_결과_배열) => 덧셈_곱셈_결과_배열);
}

function 점수_구하기(수_배열) {
  return Math.max(...연산_결과_구하기(수_배열));
}

function 승부_결과_구하기(선수1의_점수, 선수2의_점수) {
  if (선수1의_점수 > 선수2의_점수) return 1;

  if (선수1의_점수 < 선수2의_점수) return 2;

  return 0;
}

