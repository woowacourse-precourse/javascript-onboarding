function threeSixNineCount(num) {
  let count = 0;
  for (let i = 0; i < String(num).length; i++) {
    if (
      String(num)[i] === '3' ||
      String(num)[i] === '6' ||
      String(num)[i] === '9'
    ) count++;  
  }
  return count;
}

