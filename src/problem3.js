// 의사 코드 작성
// 1. 인자 number 만큼의 크기를 갖는 배열 생성
// 2. 배열에 순차적으로 1부터 number 까지 넣기
// 3. 배열의 모든 요소를 하나의 문자열로 바꾸기
// 4. 문자열을 한 문자 단위로 나누기
// 5. 3, 6, 9 문자가 나올 때마다 세기 

function problem3(number) {
  let answer = 0;
  let connectStr = Array(number).fill().map((val, idx) => idx + 1).join("");

  connectStr.split("").forEach(el => {
    if (el === '3' || el === '6' || el === '9') {
      answer += 1;
    } 
  });

  return answer;
}

module.exports = problem3;
