/** 기능 목록
 * 1. 청개구리 사전을 만든다.
 * 2. 입력된 단어를 알파벳 별로 청개구리 사전에서 찾아 result에 추가한다.
 * 3. 청개구리 사전에 없는 알파벳은 그대로 result에 추가한다. (ex. 공백)
 * 4. 완성된 result를 반환한다.
 */

function problem4(word) {
  const treefrogDictionary = {
    A: "Z",
    B: "Y",
    C: "X",
    D: "W",
    E: "V",
    F: "U",
    G: "T",
    H: "S",
    I: "R",
    J: "Q",
    K: "P",
    L: "O",
    M: "N",
    N: "M",
    O: "L",
    P: "K",
    Q: "J",
    R: "I",
    S: "H",
    T: "G",
    U: "F",
    V: "E",
    W: "D",
    X: "C",
    Y: "B",
    Z: "A",
    a: "z",
    b: "y",
    c: "x",
    d: "w",
    e: "v",
    f: "u",
    g: "t",
    h: "s",
    i: "r",
    j: "q",
    k: "p",
    l: "o",
    m: "n",
    n: "m",
    o: "l",
    p: "k",
    q: "j",
    r: "i",
    s: "h",
    t: "g",
    u: "f",
    v: "e",
    w: "d",
    x: "c",
    y: "b",
    z: "a",
  };

  let result = "";
  for (let i = 0; i < word.length; i++) {
    if (treefrogDictionary[word[i]]) {
      result += treefrogDictionary[word[i]];
    } else {
      result += word[i];
    }
  }
  return result;
}

module.exports = problem4;
