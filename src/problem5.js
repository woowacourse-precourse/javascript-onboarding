function problem5(money) {
  /**
   * 1. [50000, 10000, 5000, 1000, 500, 100, 50, 10, 1]로 구성된 배열 생성
   * 2. answer = money로 설정한 후 위에 생성한 monetaryUnit로 map 함수를 돌려 몫은 맵핑하고 answer = 나머지로 설정
   * 3. 2번 과정 끝까지 반복
   */
  const monetaryUnit = [50000, 10000, 5000, 1000, 500, 100, 50, 10, 1];
  let change = money;
  const answer = monetaryUnit.map((unit) => {
    const count = Math.floor(change / unit);
    change = Math.floor(change % unit);
    return count;
  });
  return answer;
}

module.exports = problem5;
